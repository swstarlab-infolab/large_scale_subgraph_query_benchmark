SELECT count(*)
FROM E0 AS E1
JOIN E0 AS E7
  ON E1.Id2 = E7.Id1
JOIN E0 AS E13
  ON E7.Id2 = E13.Id1
JOIN E0 AS E19
  ON E13.Id2 = E19.Id1
JOIN E0 AS E20
  ON E19.Id2 = E20.Id1
  AND E20.Id2 = E1.Id1
JOIN E0 AS E2
  ON E2.Id1 = E1.Id1
  AND E2.Id2 = E13.Id1
WHERE E1.Id1 != E13.Id2
  AND E1.Id2 != E13.Id2
  AND E1.Id2 != E19.Id2
  AND E7.Id2 != E19.Id2;